#Ejmeplo "while"
contador = 5

while contador > 0:
    print(f"Tiempo restante: {contador} segundos")
    contador -= 1

print("¡Tiempo terminado!")

#Ejemplo "for"
for num in range(2, 21, 2):  # Empezamos en 2, terminamos en 20, avanzamos de 2 en 2
    print(num, end=" ")  # Imprime en una sola línea

